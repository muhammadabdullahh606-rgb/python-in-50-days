print("========== Day 03: Data Types ==========\n")

# ----------------------------------------------------
# 1. Integer (int)
# ----------------------------------------------------

print("1. Integer (int)")

age = 21
students = 35

print(age)
print(students)
print(type(age))

print("\n---------------------------------\n")

# ----------------------------------------------------
# 2. Float (float)
# ----------------------------------------------------

print("2. Float (float)")

height = 5.9
price = 499.99

print(height)
print(price)
print(type(price))

print("\n---------------------------------\n")

# ----------------------------------------------------
# 3. String (str)
# ----------------------------------------------------

print("3. String (str)")

name = "Abdullah"
country = "Pakistan"

print(name)
print(country)
print(type(name))

print("\n---------------------------------\n")

# ----------------------------------------------------
# 4. Boolean (bool)
# ----------------------------------------------------

print("4. Boolean (bool)")

is_logged_in = True
is_raining = False

print(is_logged_in)
print(is_raining)
print(type(is_logged_in))

print("\n---------------------------------\n")

# ----------------------------------------------------
# 5. Comparing Similar Values
# ----------------------------------------------------

print("5. Comparing Similar Values")

number = 20
text_number = "20"

print(number)
print(type(number))

print(text_number)
print(type(text_number))

print("\n---------------------------------\n")

# ----------------------------------------------------
# 6. Checking Data Types
# ----------------------------------------------------

print("6. Checking Data Types")

print(type(100))
print(type(10.5))
print(type("Python"))
print(type(True))

print("\n---------------------------------\n")

# ----------------------------------------------------
# 7. More Examples
# ----------------------------------------------------

print("7. More Examples")

temperature = -5
salary = 85000.75
language = "Python"
is_student = True

print(temperature, "-", type(temperature))
print(salary, "-", type(salary))

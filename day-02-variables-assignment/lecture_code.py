print("========== Day 02: Variables & Assignment ==========\n")

# ----------------------------------------------------
# 1. Creating Variables
# ----------------------------------------------------

print("1. Creating Variables")

name = "Abdullah"
age = 21

print(name)
print(age)

print("\n---------------------------------\n")

# ----------------------------------------------------
# 2. Variables vs Strings
# ----------------------------------------------------

print("2. Variables vs Strings")

city = "Lahore"

print(city)       # Prints the value stored in the variable
print("city")     # Prints the text 'city'

print("\n---------------------------------\n")

# ----------------------------------------------------
# 3. Updating Variables
# ----------------------------------------------------

print("3. Updating Variables")

score = 50
print("Initial Score:", score)

score = 75
print("Updated Score:", score)

score = 100
print("Final Score:", score)

print("\n---------------------------------\n")

# ----------------------------------------------------
# 4. Assigning One Variable to Another
# ----------------------------------------------------

print("4. Assigning One Variable to Another")

x = 10
y = x

print("x =", x)
print("y =", y)

print("\n---------------------------------\n")

# ----------------------------------------------------
# 5. Changing One Variable Doesn't Change the Other
# ----------------------------------------------------

print("5. Changing One Variable")

x = 100

print("x =", x)
print("y =", y)

print("\n---------------------------------\n")

# ----------------------------------------------------
# 6. Meaningful Variable Names
# ----------------------------------------------------

print("6. Meaningful Variable Names")

product_name = "Laptop"
product_price = 75000
quantity = 2

print(product_name)
print(product_price)
print(quantity)

print("\n---------------------------------\n")

# ----------------------------------------------------
# 7. Variable Naming Examples
# ----------------------------------------------------

print("7. Valid Variable Names")

student_name = "Ali"
student2 = "Ahmed"
_marks = 95

print(student_name)
print(student2)
print(_marks)

print("\n---------------------------------\n")

